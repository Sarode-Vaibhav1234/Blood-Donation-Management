    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String adhar = jTextField1.getText();

        if (adhar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Aadhar No");
            return;
        }

        if (!isValidad(adhar)) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Aadhar No.\nAadhar No contains only 12 Numbers.");
            return;
        }

        Check_Adhar ca = new Check_Adhar();

        if (ca.isAdhar(st, adhar)) {
            JOptionPane.showMessageDialog(null, "Entered Aadhar No Is Already Exist");

            ArrayList<String> a1 = new DataFetcher().getLastDate(st, adhar);
            if (a1.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No previous donation date found.");
                return;
            }

            long small = new Cal_Difference_Between_Dates().getDateDiff(a1, date);
            if (small >= 45) {
                ArrayList a = new DataFetcher().getDonorData(st, adhar);

                if (a.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "No donor data found for the provided Aadhar number.");
                    return;
                }

                if (a.size() < 10) {
                    JOptionPane.showMessageDialog(null, "Incomplete donor data retrieved. Please check database.");
                    return;
                }

                jPanel2.setVisible(true);

                String gender = (String) a.get(0);
                String he = (String) a.get(1);
                String we = (String) a.get(2);
                String B_group = (String) a.get(3);
                String age = (String) a.get(4);
                String name = (String) a.get(5);
                String mob = (String) a.get(6);
                String email = (String) a.get(7);
                String address = (String) a.get(8);
                String DOB = (String) a.get(9);

                // Set Gender
                if ("Male".equals(gender)) {
                    jRadioButton1.setSelected(true);
                } else if ("Female".equals(gender)) {
                    jRadioButton2.setSelected(true);
                } else if ("Other".equals(gender)) {
                    jRadioButton3.setSelected(true);
                }

                jTextField3.setText(he);
                jTextField4.setText(we);
                jComboBox1.setSelectedItem(B_group);
                jTextField5.setText(age);
                jTextField2.setText(name);
                jTextField7.setText(mob);
                jTextField8.setText(email);
                jTextArea1.setText(address);

                // Validate and Split DOB
                if (DOB == null || DOB.isEmpty() || !DOB.contains("/")) {
                    JOptionPane.showMessageDialog(null, "Invalid DOB format in database.");
                    return;
                }

                String[] s = DOB.split("/");
                if (s.length == 3) {
                    String d = s[0];
                    String m = s[1];
                    String y = s[2];
                    jComboBox2.setSelectedItem(y);
                    jComboBox3.setSelectedItem(m);
                    jComboBox4.setSelectedItem(d);
                } else {
                    JOptionPane.showMessageDialog(null, "Unexpected DOB format.");
                }
            } else {
                long diff = 45 - small;
                JOptionPane.showMessageDialog(null, "Dear Donor, you can donate blood after " + diff + " days.");
            }
        } else {
            jPanel2.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    public boolean isValidad(String ad) {
        boolean flag = false;

        if (!ad.isEmpty()) {
            String strPattern = "^[2-9][0-9]{11}$";
            if (ad.matches(strPattern)) {
                flag = true;
            }
        }
        return flag;
    }

