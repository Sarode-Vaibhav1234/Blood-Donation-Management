    public boolean isValidEmail(String email) {
        boolean flag = false;
        if (!email.isEmpty()) {
            if (email.contains(".") && email.contains("@")) {
                flag = true;
            }
        }
        return flag;
    }

    public boolean isValidNO(String mobile) {
        boolean flag = false;
        if (!mobile.isEmpty()) {
            if (mobile.length() == 10) {
                long num = Long.parseLong(mobile);
                flag = true;
            }
        }
        return flag;
    }
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String adhar = jTextField1.getText();
        String gender = "";
        if (jRadioButton1.isSelected()) {
            gender = "Male";
        }
        if (jRadioButton2.isSelected()) {
            gender = "Female";
        }
        if (jRadioButton2.isSelected()) {
            gender = "Other";
        }
        String he = jTextField3.getText();
        String we = jTextField4.getText();
        String age = jTextField5.getText();
        String se = "Select";
        String bg = (String) jComboBox1.getSelectedItem();
        String bmi = jTextField6.getText();
        String name = jTextField2.getText();
        String mob = jTextField7.getText();
        String email = jTextField8.getText();
        String address = jTextArea1.getText();
        String year = (String) jComboBox2.getSelectedItem();
        String month = (String) jComboBox3.getSelectedItem();
        String day = (String) jComboBox4.getSelectedItem();
        String t_no = jTextField9.getText();
        String DOB = day + "/" + month + "/" + year;
        System.out.println(DOB);
        String t_status = jTextField10.getText();
        String date_time = jTextField11.getText();
        String b_vol = jTextField12.getText();
        if (gender.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Select Gender");
        } else if (he.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Height");
        } else if (we.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Weight");
        } else if (bg.equals(se)) {
            JOptionPane.showMessageDialog(null, "Please Select Blood Group");
        } else if (age.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Age");
        } else if (bmi.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Calculate BMI.");
        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Name");
        } else if (mob.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Mobile no");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter email");
        } else if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(null, "Please Enter Email ID");
        } else if (!isValidNO(mob)) {
            JOptionPane.showMessageDialog(null, "Please Enter Mobile Number");
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "please Enter Address");
        } else if (year.equals("Years")) {
            JOptionPane.showMessageDialog(null, "Please Select Year");
        } else if (month.equals("Months")) {
            JOptionPane.showMessageDialog(null, "Please Select Month");
        } else if (day.equals("Days")) {
            JOptionPane.showMessageDialog(null, "Please Select Day");
        } else {
            New_Donorr nd = new New_Donorr();
            if (nd.isInsert(st, adhar, gender, he, we, bg, age, bmi, name, mob, email, address, DOB, t_no, t_status, date_time, b_vol)) {
                JOptionPane.showMessageDialog(null, "Donor Data Inserted Successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Donor In Insertion");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

