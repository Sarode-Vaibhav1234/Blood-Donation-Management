    public boolean isValidAge(String age) {
        boolean flag = false;
        if (!age.isEmpty()) {
            int a = Integer.parseInt(age);
            if (a > 18 && a < 65) {
                try {
                    long num = Long.parseLong(age);
                    flag = true;
                } catch (Exception e) {
                    System.out.println("Exception :" + e);
                }
            }
        }
        return flag;
    }
